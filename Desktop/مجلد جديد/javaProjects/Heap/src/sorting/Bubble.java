package sorting;

import java.util.Arrays;

/**
 *
 * @author قتادة
 */
public class Bubble {

    public static void main(String[] args) {
        int[] a = {32, 3, 1, 7, 4, 5};
        for (int i = 0; i < a.length - 1; i++) {
            for (int j = 0; j < a.length - i - 1; j++) {
                if (a[j] > a[j + 1]) {
                    int temp = a[j];
                    a[j] = a[j + 1];
                    a[j + 1] = temp;
                }
            }
        }
        System.out.println(Arrays.toString(a));
    }
}
